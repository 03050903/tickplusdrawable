package com.flavienlaurent.tickplusdrawable;

public class FloatPoint {
    float x;
    float y;

    public FloatPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "FloatPoint{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
